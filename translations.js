function setLanguage(lang) {
    localStorage.setItem('siteLang', lang);
    document.documentElement.lang = lang === 'pt' ? 'pt-BR' : lang === 'es' ? 'es' : 'en';

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = translations[lang][key];
            } else {
                el.innerHTML = translations[lang][key];
            }
        }
    });

    // Update active flag in selector
    document.querySelectorAll('.lang-option').forEach(opt => {
        opt.classList.toggle('active', opt.getAttribute('data-lang') === lang);
    });

    // Update current flag display
    const currentFlag = document.querySelector('.lang-current-flag');
    const currentLabel = document.querySelector('.lang-current-label');
    if (currentFlag) {
        const flags = { pt: 'üáßüá∑', en: 'üá∫üá∏', es: 'üá™üá∏' };
        const labels = { pt: 'PT', en: 'EN', es: 'ES' };
        currentFlag.textContent = flags[lang];
        if (currentLabel) currentLabel.textContent = labels[lang];
    }
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('siteLang') || 'pt';
    setLanguage(savedLang);

    // Language dropdown toggle
    const langSelector = document.querySelector('.lang-selector');
    if (langSelector) {
        langSelector.querySelector('.lang-current').addEventListener('click', (e) => {
            e.stopPropagation();
            langSelector.classList.toggle('open');
        });

        document.querySelectorAll('.lang-option').forEach(opt => {
            opt.addEventListener('click', (e) => {
                e.stopPropagation();
                setLanguage(opt.getAttribute('data-lang'));
                langSelector.classList.remove('open');
            });
        });

        document.addEventListener('click', () => {
            langSelector.classList.remove('open');
        });
    }
});
